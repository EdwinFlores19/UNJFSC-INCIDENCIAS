package MODELO;

public class periferico {

    private int id_periferico;
    private String periferico;

    public periferico() {
    }

    public periferico(int id_periferico, String periferico) {
        this.id_periferico = id_periferico;
        this.periferico = periferico;
    }

    public int getId_periferico() {
        return id_periferico;
    }

    public void setId_periferico(int id_periferico) {
        this.id_periferico = id_periferico;
    }

    public String getPeriferico() {
        return periferico;
    }

    public void setPeriferico(String periferico) {
        this.periferico = periferico;
    }



}
